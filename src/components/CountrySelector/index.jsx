const CountrySelector = ({ selectedCountry, onCountryChange }) => {
  const [anchorEl, setAnchorEl] = useState(null);
  const open = Boolean(anchorEl);

  const handleClick = (event) => {
    setAnchorEl(event.currentTarget);
  };

  const handleClose = () => {
    setAnchorEl(null);
  };

  const handleCountrySelect = (country) => {
    onCountryChange(country);
    handleClose();
  };

  return (
    <div>
      <Button
        onClick={handleClick}
        variant="outlined"
        className="!min-w-[120px] !justify-between !border-gray-300 !text-gray-700"
      >
        <span className="mr-2">{selectedCountry.flag}</span>
        <span className="text-sm">{selectedCountry.dialCode}</span>
        <span className="ml-2 text-gray-500">â–¼</span>
      </Button>
      <Menu
        anchorEl={anchorEl}
        open={open}
        onClose={handleClose}
        PaperProps={{
          style: {
            maxHeight: 300,
            width: 250,
          },
        }}
      >
        {countries.map((country) => (
          <MenuItem
            key={country.code}
            onClick={() => handleCountrySelect(country)}
            selected={selectedCountry.code === country.code}
          >
            <ListItemIcon>
              <span className="text-lg">{country.flag}</span>
            </ListItemIcon>
            <ListItemText
              primary={country.name}
              secondary={country.dialCode}
              primaryTypographyProps={{ fontSize: '14px' }}
              secondaryTypographyProps={{ fontSize: '12px', color: 'gray' }}
            />
          </MenuItem>
        ))}
      </Menu>
    </div>
  );
};

export default CountrySelector; 